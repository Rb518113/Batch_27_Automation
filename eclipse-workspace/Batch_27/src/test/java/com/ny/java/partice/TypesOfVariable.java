package com.ny.java.partice;

public class TypesOfVariable {
//After the class and before the method if 
//we declare the variable help of static keyword
	//that is call static /global/ class level variable   
			
	static String names = "USA"; // static /global/ class level variable  
	
	
//After the class and before the method if 
//we declare the variable that is call instance variable  
	
	String name ="Smart Tech"; //instance variable
	
	
	
	public static void main(String[] args) {
//If we declare the variable inside the any method that is 
		//              local variable 

		String name ="Smart Tech"; // local variable 

	}
	
	
	
	

}
